def total_cost(items):
    total = 0
    for price in items.values():
        total += price
    return total

# Dictionary of items and prices
shopping = {"apple": 1.5, "banana": 0.8, "milk": 2.0}

# Call the function and print the result
print("Total cost:", total_cost(shopping))

